package javaforloops;

public class loop3 {
	static public void main(String[] args) {

		for(int k=20;k<28;k++) {
			System.out.println(k);
		}
		System.out.println("*************Reverse****************");
		for(int i=55;i>50;i--) {
			System.out.println(i);
		}
		int asciValue='A';
		System.out.println(asciValue);
	}
}